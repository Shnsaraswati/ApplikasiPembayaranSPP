    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String query = "UPDATE data_murid set nama=?,kelas=?,kota_kelahiran=?,ttl=?,alamat=? WHERE nis=?";
        try {
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, txtNama.getText());
            stmt.setString(2, txtKelas.getText());
            stmt.setString(3, txtKotaKel.getText());
            stmt.setString(4, txtTTL.getText());
            stmt.setString(5, txtAlamat.getText());
            stmt.setString(6, txtNis.getText());
            stmt.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Berhasil Update Data");
        } catch (Exception e) {
            System.out.println("error : " + e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

