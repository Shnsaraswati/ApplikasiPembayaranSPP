    public void dataTable() {
        Object[] baris = {"NIS","NAMA","KELAS","KOTA KELAHIRAN","TTL","ALAMAT"};
        tabmode = new DefaultTableModel(null, baris);
        tblSiswa.setModel(tabmode);
        
        String query = "SELECT * from data_murid";
        
        try {
            Statement stmt = conn.createStatement();
            ResultSet data = stmt.executeQuery(query);
            
            while(data.next())
            {
                String nis = data.getString("NIS");
                String nama = data.getString("nama");
                String kelas = data.getString("kelas");
                String kokel = data.getString("kota_kelahiran");
                String ttl = data.getString("TTL");
                String alamat = data.getString("alamat");
                
                String[] row = {nis,nama,kelas,kokel,ttl,alamat};
                tabmode.addRow(row);
            }
        } catch (Exception e) {
            System.out.println("Error : " + e);
        }
    }
    
